package Com.basicJava;

public class forloopexample2 {
public static void main(String[] args) {
	int a;
	for(int var=5;var>=2 ; var--)
	{
		System.out.println(var+"LIVETECHSHIVA");
	}
	for(a=-3;a>=2;a--)
	{
		System.out.println("++++++");
	}
	for(int i=-9;i<=-6;i++)
	{
		System.out.println("*************");
	}
	for(int i=-15; a >-19;a--)
	{
		System.out.println(a+"QTP");
	}
	System.out.println("##########");
	for(int p=10;p>-10;p=p-3)
	{
		System.out.println(p+"UFT");
	}
}
}

